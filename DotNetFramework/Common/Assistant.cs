        /// <summary>
        /// 生成指定长度随机数
        /// </summary>
        /// <param name="n"></param>
        /// <returns></returns>
        public string GenerateCode(int n)
        {
            int number;
            char code;
            string checkCode = String.Empty;
            Random random = new Random();
            for (int i = 0; i < n; i++)
            {
                number = random.Next();
                if (number % 2 == 0)
                {
                    code = (char)('0' + (char)(number % 10));
                }
                else
                {
                    code = (char)('A' + (char)(number % 26));
                }
                checkCode += code.ToString();
            }
            return checkCode;
